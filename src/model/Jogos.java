public class Jogos {
    
    private int jogo;
    private int placar;
    private int minimo;
    private int maximo;
    private int quebramin;
    private int quebramax;

    public int getJogo() {
        return jogo;
    }

    public void setJogo(int jogo) {
        this.jogo = jogo;
    }

    public int getPlacar() {
        return placar;
    }

    public void setPlacar(int placar) {
        this.placar = placar;
    }

    public int getMinimo() {
        return minimo;
    }

    public void setMinimo(int minimo) {
        this.minimo = minimo;
    }

    public int getMaximo() {
        return maximo;
    }

    public void setMaximo(int maximo) {
        this.maximo = maximo;
    }

    public int getQuebramin() {
        return quebramin;
    }

    public void setQuebramin(int quebramin) {
        this.quebramin = quebramin;
    }

    public int getQuebramax() {
        return quebramax;
    }

    public void setQuebramax(int quebramax) {
        this.quebramax = quebramax;
    }
    
    
    
}
